public class ArithmeticOperations {

    public static void main(String[] args) {
        // Arithmetic Operations

        /*
           1. +
           2. -
           3. *
           4. /
           5. %

         */

        int a = 50;
        int b = 20;
        int total;
        int sub;
        int multi;
        int div;
        int mod;

        total = a + b;
        System.out.println("The total of a + b = " + total);

        sub = a - b;
        System.out.println("The Subtraction of a - b = " + sub);

        multi = a * b;
        System.out.println("The Multiplication of a * b = " + multi);

        div = a / b;
        System.out.println("The Division of a / b = " + div);

        mod = a % b;
        System.out.println("The Modulus of a % b = " + mod);

    }
}
