public class DataTypes {

    public static void main(String[] args) {

        // booleans (True/False)
        boolean bool = true; // Variable 1. Data type  2. Variable name  3. value
        System.out.println(bool); // true

        // Integers (100, 12365, 14789654)
        byte by = 100;
        System.out.println(by);

        short srt = 1100;
        System.out.println(srt);

        int in = 1000000;
        System.out.println(in);

        long ln = 1698745689552L;
        //long ln2; 0
        System.out.println(ln);

        // fractional numbers (123.321, 1478965.258741)
        float fl = 12.32F;
        System.out.println(fl);

        double db = 12365.25874;
        System.out.println(db);

        // characters (A , B, #, &, a , b)
        char ch = 'A';
        System.out.println(ch);

        // Combination of characters (Words, BCAS, Sainthamaruthu)
        String str = "BCAS";
        System.out.println(str);

    }
}
